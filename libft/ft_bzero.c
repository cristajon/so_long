#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*l;

	i = 0;
	l = ((char *)&s[i]);
	while (i < n && n != 0)
	{
		l[i] = '\0';
		i++;
	}
}
